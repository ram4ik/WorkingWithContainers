import SwiftUI

struct ContentView: View {
    @State var selectedView = 1
    var body: some View {
        TabView(selection: $selectedView) {
            Text("First View")
                .tabItem {
                    Image(systemName: "1.circle")
                    Text("First")
                }.tag(0)
            Text("Second View")
                .tabItem {
                    Image(systemName: "2.circle")
                    Text("Second")
                }.tag(1)
        }
    }
}

